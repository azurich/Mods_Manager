// Fonction utilitaire pour créer une icône
function createIcon(name, options = {}) {
  const {
    size = 16,
    className = '',
    color = 'currentColor'
  } = options;
  
  if (!icons[name]) {
    console.warn(`Icon "${name}" not found`);
    return createIcon('info', options);
  }
  
  const iconWrapper = document.createElement('span');
  iconWrapper.className = `inline-flex items-center justify-center ${className}`;
  iconWrapper.style.width = `${size}px`;
  iconWrapper.style.height = `${size}px`;
  iconWrapper.style.color = color;
  
  iconWrapper.innerHTML = icons[name];
  
  // Appliquer la taille au SVG
  const svg = iconWrapper.querySelector('svg');
  if (svg) {
    svg.style.width = '100%';
    svg.style.height = '100%';
  }
  
  return iconWrapper;
}

// Export des icônes
window.Icons = {
  createIcon,
  icons,
  ...Object.keys(icons).reduce((acc, name) => {
    acc[name] = (options) => createIcon(name, options);
    return acc;
  }, {})
};